DISCLAIMER_HE = (
    "לפני שמתחילים, חשוב שתדע - אני לא מטפלת ולא רופאה. "
    "אני אוזן קשבת. אם משהו דחוף, ער\"ן בקו 1201 ונט\"ל בקו 1 800 363 363 "
    "כאן עבורך 24 שעות ביממה."
)


def build_system_prompt(rag_context: str | None = None) -> str:
    """Compose the full system prompt with optional retrieved context."""
    prompt = SYSTEM_PROMPT_HE
    if rag_context:
        prompt += (
            "\n\n## רקע רלוונטי (לשימוש פנימי - אל תצטט)\n"
            "המידע הבא נלקח ממאמרים מקצועיים ויכול לעזור לך להבין טוב יותר "
            "את ההקשר. תרגם אותו לשפה אנושית, אל תצטט.\n\n"
            f"{rag_context}\n"
        )
    return prompt
